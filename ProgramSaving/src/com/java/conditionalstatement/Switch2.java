package com.java.conditionalstatement;

public class Switch2 
{
  public static void main(String[] args)
  {
	String options= "TVS";
	
	 switch(options)
	 
	 {
	 case "honda":
		     System.out.println("choosing bike honda");
		   break;
	 case "hero":
		     System.out.println("find hero bike");
		    break;
		    
	 case "TVS":
		   System.out.println( "i found tvs bike");
		   break;
	 default:
		   System.out.println("No result");
		   
	 }
	 
	 
}
}
